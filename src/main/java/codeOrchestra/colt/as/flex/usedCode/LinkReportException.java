package codeOrchestra.colt.as.flex.usedCode;

/**
 * @author Alexander Eliseyev
 */
public class LinkReportException extends Exception {

    public LinkReportException() {
    }

    public LinkReportException(String message) {
        super(message);
    }

    public LinkReportException(String message, Throwable cause) {
        super(message, cause);
    }

    public LinkReportException(Throwable cause) {
        super(cause);
    }

    public LinkReportException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
